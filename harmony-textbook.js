// ===== УЧЕБНИК: БАЗА СОЛЬФЕДЖИО + ГАРМОНИЯ + ЦЕПОЧКИ =====
// Подгружается в системный промпт в режиме нотации (вместе с HARMONY_RULEBOOK).

(function (global) {
    'use strict';

    const THEORY_BASICS = `

############################################
###  БАЗА СОЛЬФЕДЖИО (шпаргалка для ИИ)   ###
############################################
При ЛЮБОМ «построй / сделай / напиши» — сначала определи тип упражнения, потом выведи ПОЛНЫЙ комплект нот.
Один пример вместо полного задания = ОШИБКА. Текст — 1–2 предложения, ответ — в [[NOTATION:...]].

--- 1. ГАММЫ ---
• «Построй гамму X» без уточнения → натуральная + гармоническая + мелодическая (вверх и вниз для мел.).
• barlines:"none", timeSignature:"" или "none".
• Мелодическая: 15 нот (вверх 8 + вниз 7) в одном блоке.
• Минор → keySignature = relative major (a-m → C, g-m → Bb …).

--- 2. ТРИТОНЫ ---
• «Тритоны в X» БЕЗ слова «натуральн» → ГАРМОНИЧЕСКАЯ форма = 2 пары = 8 созвучий (ув.4+ум.5 с разрешениями).
• «Натуральные тритоны» → 1 пара = 4 созвучия.
• barlines:"manual" + barAfter:true после каждой пары «интервал → разрешение».
• Разрешение: ув.4 → секста (м.6/б.6), ум.5 → терция (м.3/б.3). НИКОГДА в квинту/кварту.

--- 3. ХАРАКТЕРНЫЕ ИНТЕРВАЛЫ ---
• ВСЕ 4 пары в гармоническом ладу: ув.2→ч.4, ум.7→ч.5, ув.5→б.6, ум.4→м.3 = 8 созвучий.
• barlines:"manual", barAfter после каждой пары.

--- 4. ГЛАВНЫЕ ТРЕЗВУЧИЯ (T, S, D) ---
• 3 трезвучия; с обращениями → T53 T6 T64, S53 S6 S64, D53 D6 D64 (9 аккордов).
• label = функция; ноты = реальное строение. Проверяй каждый аккорд.

--- 5. D7 (доминантсептаккорд) ---
• Подписи ТОЛЬКО латиницей: D7, D6/5, D4/3, D2 (не «Д7», не «Д65»).
• «D7 с обращениями» → 4 формы. «+ разрешения» → каждая форма + тоника (8 аккордов).
• barlines:"manual" при разрешениях.

--- 6. ВИДЫ ТРЕЗВУЧИЙ ОТ НОТЫ ---
• 4 аккорда: мажорное, минорное, увеличенное, уменьшённое (Б53/М53/Ув53/Ум53 или M/m/A/d).

--- 7. ЦЕПОЧКИ АККОРДОВ (см. раздел ниже) ---
• Цепочка 1 — мажор, 9 аккордов. Цепочка 2 — минор, 11 аккордов.
• barlines:"none". Каждый аккорд — label. НЕ сокращай цепочку.

=== ЗОЛОТОЕ ПРАВИЛО ===
Если запрос распознан движком theory.js — система подставит эталонные ноты. Не выдумывай свои, если не уверен на 100%.
`;

    const CHAINS_TEXTBOOK = `

############################################
###  ЦЕПОЧКИ АККОРДОВ — КАК СТРОИТЬ       ###
############################################

=== КОГДА КАКУЮ ЦЕПОЧКУ ===
• МАЖОР (C-dur, G-dur, D-dur …) → Цепочка 1 (по умолчанию).
• МИНОР (a-moll, e-moll, d-moll …) → Цепочка 2 (по умолчанию).
• «Цепочка 1 / chain 1» → только схема 1 (мажор).
• «Цепочка 2 / chain 2» → только схема 2 (минор).

=== ЦЕПОЧКА 1 (МАЖОР) — 9 АККОРДОВ, СТРОГО В ЭТОМ ПОРЯДКЕ ===
T53 – S64 – VII7 – D65 – T53 – S6 – K64 – D7 – T53

Расшифровка:
1. T53 — тоника основной вид
2. S64 — субдоминанта квартсекстаккорд (в гарм. мажоре: s53 с bVI)
3. VII7 — уменьшённый вводный септаккорд (VII°7)
4. D65 — доминантсептаккорд 1-е обращение (6/5)
5. T53 — тоника
6. S6 — субдоминанта секстаккорд
7. K64 — кадансовый квартсекстаккорд (T6/4 на V, перед D)
8. D7 — доминантсептаккорд основной вид
9. T53 — тоника (закрытие)

Пример в C-dur (эталон нот): c-e-g | c-f-a | b-d-f-a | b-d-f-g | c-e-g | a-c-f | g-c-e | g-b-d-f | c-e-g

=== ЦЕПОЧКА 2 (МИНОР) — 11 АККОРДОВ ===
t53 – d6 – s6 – D53 – D2 – t6 – II7 – D43 – t53 – s64 – t53

Расшифровка:
1. t53 — тоника минор
2. d6 — доминанта секстаккорд (D53 в 6 положении)
3. s6 — субдоминанта секстаккорд
4. D53 — доминанта мажорная (гарм. минор, V)
5. D2 — D7 3-е обращение (секундаккорд)
6. t6 — тоника секстаккорд
7. II7 — II ступень септаккорд (полууменьш.)
8. D43 — D7 2-е обращение (терцквартаккорд)
9. t53 — тоника
10. s64 — субдоминанта квартсекстаккорд
11. t53 — тоника

=== ФОРМАТ JSON ДЛЯ ЦЕПОЧКИ ===
[[NOTATION:{"clef":"treble","keySignature":"G","barlines":"none","timeSignature":"","notes":[
  {"keys":["g/4","b/4","d/5"],"duration":"w","label":"T53"},
  {"keys":["g/4","c/5","e/5"],"duration":"w","label":"S64"},
  ... ровно 9 или 11 аккордов ...
  {"keys":["g/4","b/4","d/5"],"duration":"w","label":"T53"}
]}]]

Правила:
• clef:"treble", barlines:"none", timeSignature:"" (без размера).
• Каждый аккорд: duration "w" (или "h"), keys[] = 3–4 ноты (септаккорды = 4), label обязателен.
• labels в цепочке 1 — заглавные T/S/D; в цепочке 2 — t/s/D смешанно как в схеме.
• Один блок, все аккорды подряд — рендерер сам переносит на несколько строк.
• ЗАПРЕЩЕНО: 1–3 аккорда «для примера», пропуск VII7/K64, неверный порядок, label не совпадает с нотами.

=== САМОПРОВЕРКА ЦЕПОЧКИ ===
1) Верная тональность (keySignature)?
2) Правильное число аккордов (9 или 11)?
3) Порядок labels совпадает со схемой?
4) Каждый аккорд: ноты = реальное строение функции (сверь с разделами T/S/D, D7 в HARMONY_RULEBOOK)?
`;

    const HARMONIZATION_TEXTBOOK = `

############################################
###  ГАРМОНИЗАЦИЯ МЕЛОДИИ (SATB)          ###
############################################
Формат: "layout":"satb" — скрипичный ключ (S+A) + басовый ключ (T+B).
Полная гармонизация всех тактов, не один аккорд. См. предыдущие правила SATB в системном промпте.
`;

    function getPrompt() {
        return THEORY_BASICS + CHAINS_TEXTBOOK + HARMONIZATION_TEXTBOOK;
    }

    function getBasicsPrompt() {
        return THEORY_BASICS;
    }

    function getChainsPrompt() {
        return CHAINS_TEXTBOOK;
    }

    global.SolfHarmonyTextbook = {
        getPrompt,
        getBasicsPrompt,
        getChainsPrompt,
        THEORY_BASICS,
        CHAINS_TEXTBOOK,
        HARMONIZATION_TEXTBOOK
    };
})(typeof window !== 'undefined' ? window : globalThis);
